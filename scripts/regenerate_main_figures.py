# FIGURE 6: TIPPING POINT COMPOSITE (redesigned F10)
# ═══════════════════════════════════════════════════════
print("\n--- Figure 6: Tipping Point ---")

fig, axes = plt.subplots(2, 2, figsize=(8, 7))

# (a) Ferrihydrite SI distributions
ax = axes[0, 0]
# Compute empirical SI: log10[Fe3+] + 3*pH - log10(Ksp)
# logKsp = 4.891 for amorphous ferrihydrite
logKsp = 4.891
for label, df, color in [("2012–13", old_all, OLD_COLOR), ("2020–21", new_all, NEW_COLOR)]:
    mask = df["Fe"].notna() & df["pH"].notna() & df["Eh"].notna() & (df["Fe"] > 0)
    sub = df[mask].copy()
    # Nernst: Fe3+ fraction from total dissolved Fe
    # pe = Eh(mV) / 59.2 at 25°C
    pe = sub["Eh"] / 59.2
    # log(Fe3+/Fe2+) = pe - 13.02 (from Fe3+/Fe2+ half-reaction)
    log_ratio = pe - 13.02
    ratio = 10 ** log_ratio.clip(-10, 10)
    fe3 = sub["Fe"] * ratio / (1 + ratio) / 55.845 * 1000  # mmol/L → mol/L... mg/L to mol/L
    fe3_mol = sub["Fe"] / 55.845 / 1000 * ratio / (1 + ratio)  # mol/L
    fe3_mol = fe3_mol.clip(lower=1e-30)
